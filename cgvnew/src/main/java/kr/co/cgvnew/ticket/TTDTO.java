package kr.co.cgvnew.ticket;

public class TTDTO {
	public String getTt_num() {
		return tt_num;
	}
	public void setTt_num(String tt_num) {
		this.tt_num = tt_num;
	}
	public String getTt_date() {
		return tt_date;
	}
	public void setTt_date(String tt_date) {
		this.tt_date = tt_date;
	}
	public String getMv_num() {
		return mv_num;
	}
	public void setMv_num(String mv_num) {
		this.mv_num = mv_num;
	}
	public String getTs_num() {
		return ts_num;
	}
	public void setTs_num(String ts_num) {
		this.ts_num = ts_num;
	}
	private String tt_num;
	private String tt_date;
	private String mv_num;
	private String ts_num;
}
