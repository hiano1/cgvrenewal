package kr.co.cgvnew.event;

public class EventBoardDTO {
	private String ev_num;
	private String ev_nm;
	private String ev_sdate;
	private String ev_edate;
	private String ev_p;
	public String getEv_num() {
		return ev_num;
	}
	public void setEv_num(String ev_num) {
		this.ev_num = ev_num;
	}
	public String getEv_nm() {
		return ev_nm;
	}
	public void setEv_nm(String ev_nm) {
		this.ev_nm = ev_nm;
	}
	public String getEv_sdate() {
		return ev_sdate;
	}
	public void setEv_sdate(String ev_sdate) {
		this.ev_sdate = ev_sdate;
	}
	public String getEv_edate() {
		return ev_edate;
	}
	public void setEv_edate(String ev_edate) {
		this.ev_edate = ev_edate;
	}
	public String getEv_p() {
		return ev_p;
	}
	public void setEv_p(String ev_p) {
		this.ev_p = ev_p;
	}
	
}
